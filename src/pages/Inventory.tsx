import { useState, useEffect } from 'react';
import { Link } from 'react-router-dom';
import { 
  ArrowLeft, 
  Plus, 
  Search, 
  Package,
  AlertTriangle,
  Edit,
  Trash2,
  Calendar,
  DollarSign
} from 'lucide-react';
import { useAuth } from '../contexts/AuthContext';
import { UserDataService } from '../services/userDataService';

interface InventoryItem {
  id: string;
  farmId: string;
  category: 'seed' | 'fertilizer' | 'pesticide' | 'equipment';
  name: string;
  quantity: number;
  unit: string;
  reorderThreshold: number;
  cost: number;
  purchaseDate: Date;
  expiryDate?: Date;
  supplier: string;
  notes: string;
}

const Inventory = () => {
  const { user, profile } = useAuth();


  const [inventory, setInventory] = useState<InventoryItem[]>([]);
  const [filteredInventory, setFilteredInventory] = useState<InventoryItem[]>([]);
  const [searchTerm, setSearchTerm] = useState('');
  const [selectedCategory, setSelectedCategory] = useState<string>('all');
  const [showLowStock, setShowLowStock] = useState(false);
  const [isLoading] = useState(false); // Start with false for instant loading

  useEffect(() => {
    // Only auto-populate if user has completed their profile
    if (!user || !profile) return;

    // Check if profile is complete
    const isProfileComplete = profile.full_name && 
                             profile.primary_crop && 
                             profile.land_size && 
                             profile.location?.state;

    if (!isProfileComplete) {
      console.log('⚠️ User profile incomplete, not generating inventory data');
      setInventory([]);
      setFilteredInventory([]);
      return;
    }

    console.log('⚡ Auto-populating inventory from complete user profile data');
    
    // Extract farmer data using centralized service
    const farmerData = UserDataService.extractFarmerData(user, profile);
    
    if (!farmerData) {
      console.log('⚠️ Could not extract farmer data, profile incomplete');
      setInventory([]);
      setFilteredInventory([]);
      return;
    }
    
    console.log('📊 Generating inventory for:', {
      farmer: farmerData.fullName,
      crop: farmerData.primaryCrop,
      landSize: farmerData.landSize,
      equipment: farmerData.equipment
    });
    
    // Generate inventory using centralized service
    const autoGeneratedInventory = UserDataService.generateInventoryItems(farmerData);

    console.log('✅ Auto-generated', autoGeneratedInventory.length, 'inventory items');
    
    // Set inventory immediately for instant display
    setInventory(autoGeneratedInventory);
    setFilteredInventory(autoGeneratedInventory);
  }, [user, profile]);

  useEffect(() => {
    // Filter inventory based on search and filters
    let filtered = inventory;

    if (searchTerm) {
      filtered = filtered.filter(item => 
        item.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
        item.supplier.toLowerCase().includes(searchTerm.toLowerCase())
      );
    }

    if (selectedCategory !== 'all') {
      filtered = filtered.filter(item => item.category === selectedCategory);
    }

    if (showLowStock) {
      filtered = filtered.filter(item => item.quantity <= item.reorderThreshold);
    }

    setFilteredInventory(filtered);
  }, [inventory, searchTerm, selectedCategory, showLowStock]);

  const getCategoryIcon = (category: string) => {
    switch (category) {
      case 'seed': return '🌱';
      case 'fertilizer': return '🧪';
      case 'pesticide': return '🛡️';
      case 'equipment': return '🚜';
      default: return '📦';
    }
  };

  const getCategoryColor = (category: string) => {
    switch (category) {
      case 'seed': return 'bg-green-100 text-green-800 dark:bg-green-900/20 dark:text-green-400';
      case 'fertilizer': return 'bg-blue-100 text-blue-800 dark:bg-blue-900/20 dark:text-blue-400';
      case 'pesticide': return 'bg-red-100 text-red-800 dark:bg-red-900/20 dark:text-red-400';
      case 'equipment': return 'bg-purple-100 text-purple-800 dark:bg-purple-900/20 dark:text-purple-400';
      default: return 'bg-gray-100 text-gray-800 dark:bg-gray-900/20 dark:text-gray-400';
    }
  };

  const isLowStock = (item: InventoryItem) => item.quantity <= item.reorderThreshold;
  const isExpiringSoon = (item: InventoryItem) => {
    if (!item.expiryDate) return false;
    const daysUntilExpiry = Math.ceil((item.expiryDate.getTime() - new Date().getTime()) / (1000 * 60 * 60 * 24));
    return daysUntilExpiry <= 30 && daysUntilExpiry > 0;
  };

  const lowStockItems = inventory.filter(isLowStock);
  const expiringSoonItems = inventory.filter(isExpiringSoon);
  const totalValue = inventory.reduce((sum, item) => sum + (item.cost * item.quantity), 0);

  if (isLoading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50 dark:bg-gray-900">
        <div className="text-center">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-primary-600 mx-auto"></div>
          <p className="mt-4 text-gray-600 dark:text-gray-300">Loading inventory...</p>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gray-50 dark:bg-gray-900 pb-20">
      {/* Header */}
      <div className="bg-white dark:bg-gray-800 shadow-sm">
        <div className="container mx-auto px-4 py-4">
          <div className="flex items-center justify-between">
            <div className="flex items-center space-x-4">
              <Link to="/dashboard" className="text-gray-600 dark:text-gray-300 hover:text-primary-600">
                <ArrowLeft className="w-6 h-6" />
              </Link>
              <div>
                <h1 className="text-2xl font-bold text-gray-800 dark:text-white">Inventory Management</h1>
                <p className="text-sm text-gray-500 dark:text-gray-400">
                  Track your farm resources and supplies
                </p>
              </div>
            </div>
            <button
              onClick={() => console.log('Add item modal - coming soon')}
              className="bg-primary-600 text-white px-4 py-2 rounded-lg flex items-center space-x-2 hover:bg-primary-700"
            >
              <Plus className="w-5 h-5" />
              <span>Add Item</span>
            </button>
          </div>
        </div>
      </div>

      <div className="container mx-auto px-4 py-6">
        {/* Stats Cards */}
        <div className="grid grid-cols-1 md:grid-cols-4 gap-6 mb-6">
          <div className="bg-white dark:bg-gray-800 rounded-lg shadow-sm p-6">
            <div className="flex items-center justify-between">
              <div>
                <p className="text-sm text-gray-500 dark:text-gray-400">Total Items</p>
                <p className="text-2xl font-bold text-gray-800 dark:text-white">{inventory.length}</p>
              </div>
              <Package className="w-8 h-8 text-blue-500" />
            </div>
          </div>

          <div className="bg-white dark:bg-gray-800 rounded-lg shadow-sm p-6">
            <div className="flex items-center justify-between">
              <div>
                <p className="text-sm text-gray-500 dark:text-gray-400">Low Stock Alerts</p>
                <p className="text-2xl font-bold text-red-600">{lowStockItems.length}</p>
              </div>
              <AlertTriangle className="w-8 h-8 text-red-500" />
            </div>
          </div>

          <div className="bg-white dark:bg-gray-800 rounded-lg shadow-sm p-6">
            <div className="flex items-center justify-between">
              <div>
                <p className="text-sm text-gray-500 dark:text-gray-400">Expiring Soon</p>
                <p className="text-2xl font-bold text-yellow-600">{expiringSoonItems.length}</p>
              </div>
              <Calendar className="w-8 h-8 text-yellow-500" />
            </div>
          </div>

          <div className="bg-white dark:bg-gray-800 rounded-lg shadow-sm p-6">
            <div className="flex items-center justify-between">
              <div>
                <p className="text-sm text-gray-500 dark:text-gray-400">Total Value</p>
                <p className="text-2xl font-bold text-green-600">₹{totalValue.toLocaleString()}</p>
              </div>
              <DollarSign className="w-8 h-8 text-green-500" />
            </div>
          </div>
        </div>

        {/* Filters and Search */}
        <div className="bg-white dark:bg-gray-800 rounded-lg shadow-sm p-6 mb-6">
          <div className="flex flex-col md:flex-row md:items-center md:justify-between space-y-4 md:space-y-0">
            <div className="flex items-center space-x-4">
              <div className="relative">
                <Search className="w-5 h-5 absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400" />
                <input
                  type="text"
                  placeholder="Search inventory..."
                  value={searchTerm}
                  onChange={(e) => setSearchTerm(e.target.value)}
                  className="pl-10 pr-4 py-2 border border-gray-300 dark:border-gray-600 rounded-lg focus:ring-2 focus:ring-primary-500 focus:border-transparent bg-white dark:bg-gray-700 text-gray-900 dark:text-white"
                />
              </div>

              <select
                value={selectedCategory}
                onChange={(e) => setSelectedCategory(e.target.value)}
                className="px-4 py-2 border border-gray-300 dark:border-gray-600 rounded-lg focus:ring-2 focus:ring-primary-500 focus:border-transparent bg-white dark:bg-gray-700 text-gray-900 dark:text-white"
              >
                <option value="all">All Categories</option>
                <option value="seed">Seeds</option>
                <option value="fertilizer">Fertilizers</option>
                <option value="pesticide">Pesticides</option>
                <option value="equipment">Equipment</option>
              </select>
            </div>

            <div className="flex items-center space-x-4">
              <label className="flex items-center space-x-2">
                <input
                  type="checkbox"
                  checked={showLowStock}
                  onChange={(e) => setShowLowStock(e.target.checked)}
                  className="rounded border-gray-300 text-primary-600 focus:ring-primary-500"
                />
                <span className="text-sm text-gray-700 dark:text-gray-300">Show Low Stock Only</span>
              </label>
            </div>
          </div>
        </div>

        {/* Inventory Grid */}
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {filteredInventory.map((item) => (
            <div key={item.id} className="bg-white dark:bg-gray-800 rounded-lg shadow-sm p-6">
              <div className="flex items-start justify-between mb-4">
                <div className="flex items-center space-x-3">
                  <div className="text-2xl">{getCategoryIcon(item.category)}</div>
                  <div>
                    <h3 className="font-semibold text-gray-800 dark:text-white">{item.name}</h3>
                    <span className={`inline-block px-2 py-1 rounded-full text-xs font-medium ${getCategoryColor(item.category)}`}>
                      {item.category}
                    </span>
                  </div>
                </div>
                <div className="flex items-center space-x-2">
                  <button className="text-gray-400 hover:text-primary-600">
                    <Edit className="w-4 h-4" />
                  </button>
                  <button className="text-gray-400 hover:text-red-600">
                    <Trash2 className="w-4 h-4" />
                  </button>
                </div>
              </div>

              <div className="space-y-3">
                <div className="flex items-center justify-between">
                  <span className="text-sm text-gray-500 dark:text-gray-400">Quantity</span>
                  <div className="flex items-center space-x-2">
                    <span className={`font-semibold ${isLowStock(item) ? 'text-red-600' : 'text-gray-800 dark:text-white'}`}>
                      {item.quantity} {item.unit}
                    </span>
                    {isLowStock(item) && <AlertTriangle className="w-4 h-4 text-red-500" />}
                  </div>
                </div>

                <div className="flex items-center justify-between">
                  <span className="text-sm text-gray-500 dark:text-gray-400">Reorder Level</span>
                  <span className="text-sm text-gray-600 dark:text-gray-300">{item.reorderThreshold} {item.unit}</span>
                </div>

                <div className="flex items-center justify-between">
                  <span className="text-sm text-gray-500 dark:text-gray-400">Cost per Unit</span>
                  <span className="text-sm font-medium text-gray-800 dark:text-white">₹{item.cost}</span>
                </div>

                <div className="flex items-center justify-between">
                  <span className="text-sm text-gray-500 dark:text-gray-400">Total Value</span>
                  <span className="text-sm font-semibold text-green-600">₹{(item.cost * item.quantity).toLocaleString()}</span>
                </div>

                <div className="flex items-center justify-between">
                  <span className="text-sm text-gray-500 dark:text-gray-400">Supplier</span>
                  <span className="text-sm text-gray-600 dark:text-gray-300">{item.supplier}</span>
                </div>

                {item.expiryDate && (
                  <div className="flex items-center justify-between">
                    <span className="text-sm text-gray-500 dark:text-gray-400">Expires</span>
                    <span className={`text-sm ${isExpiringSoon(item) ? 'text-yellow-600 font-medium' : 'text-gray-600 dark:text-gray-300'}`}>
                      {item.expiryDate.toLocaleDateString()}
                      {isExpiringSoon(item) && ' ⚠️'}
                    </span>
                  </div>
                )}

                <div className="pt-3 border-t border-gray-200 dark:border-gray-700">
                  <p className="text-xs text-gray-500 dark:text-gray-400">
                    Purchased: {item.purchaseDate.toLocaleDateString()}
                  </p>
                  {item.notes && (
                    <p className="text-xs text-gray-600 dark:text-gray-300 mt-1">{item.notes}</p>
                  )}
                </div>
              </div>
            </div>
          ))}
        </div>

        {filteredInventory.length === 0 && (
          <div className="text-center py-12">
            <Package className="w-16 h-16 text-gray-400 mx-auto mb-4" />
            <h3 className="text-lg font-medium text-gray-800 dark:text-white mb-2">
              {!profile || !profile.primary_crop ? 'Complete Your Profile' : 'No items found'}
            </h3>
            <p className="text-gray-500 dark:text-gray-400">
              {!profile || !profile.primary_crop
                ? 'Please complete your farming profile to see your personalized inventory'
                : searchTerm || selectedCategory !== 'all' || showLowStock
                  ? 'Try adjusting your filters'
                  : 'Add your first inventory item to get started'
              }
            </p>
            {(!profile || !profile.primary_crop) && (
              <Link 
                to="/dashboard" 
                className="inline-block mt-4 bg-primary-600 text-white px-4 py-2 rounded-lg hover:bg-primary-700"
              >
                Complete Profile
              </Link>
            )}
          </div>
        )}
      </div>
    </div>
  );
};

export default Inventory;
